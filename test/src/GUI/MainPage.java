    private void AddbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddbtnActionPerformed

        ArrayList<String> txtStrAllList = new ArrayList<>();
        txtStrAllList.add("First Name");
        txtStrAllList.add("Surname");

        ArrayList<String> txtAllList = new ArrayList<>();
        txtAllList.add("First Name");
        txtAllList.add("Surname");
        txtAllList.add("Phone number");
        txtAllList.add("Address");
        txtAllList.add("Date of Birth");

        if (Studentbtn.isSelected()) {
            txtStrAllList.add("Major");
            txtAllList.add("Major");
        } else {
            txtStrAllList.add("Position");
            txtStrAllList.add("Department");
            txtAllList.add("Position");
            txtAllList.add("Department");
        }

        if (Studentbtn.isSelected()) {
            for (int i = 0; i < addMemberStuAllTxt().size(); i++) {
                if (!isfilled(addMemberStuAllTxt().get(i), txtAllList.get(i))) {
                    return;
                }
            }
            for (int i = 0; i < addMemberStuStrTxt().size(); i++) {
                if (!containsOnlyLetters(addMemberStuStrTxt().get(i), txtStrAllList.get(i))) {
                    return;
                }
            }
        } else {
            for (int i = 0; i < addMemberAllTxt().size(); i++) {
                if (!isfilled(addMemberAllTxt().get(i), txtAllList.get(i))) {
                    return;
                }
            }
            for (int i = 0; i < addMemberStrTxt().size(); i++) {
                if (!containsOnlyLetters(addMemberStrTxt().get(i), txtStrAllList.get(i))) {
                    return;
                }
            }
        }

        if (!numbersOnly(txtMemberPhoneNumberInput)) {
            return;
        }

        if (!malebtn.isSelected() & !femalebtn.isSelected()) {
            PopupMessage("please choose a gender");
            return;
        }

        String fname = txtMemberFirstNameInput.getText();
        String surname = txtMemberSurnameInput.getText();
        String phone = txtMemberPhoneNumberInput.getText();
        String address = txtMemberAddressInput.getText();
        String date = txtMemberDateInput.getText();
        String MajOrPos = txtMemberMajorOrPositionInput.getText();

        String gender = femalebtn.isSelected() ? "Female" : "Male";
        if (Studentbtn.isSelected()) {
            Member Student = new PolytechnicStudent(fname, surname, address, phone, gender, date, MajOrPos, txtMemberSportTeamOrDepartmentInput.getText());
            GymSystem.getMembersList().add(Student);
        } else {
            Member Staff = new PolytechnicStaff(fname, surname, address, phone, gender, date, MajOrPos, txtMemberSportTeamOrDepartmentInput.getText());
            GymSystem.getMembersList().add(Staff);
        }
        try {
            GymSystem.WriteMember();
            Member.saveMemberId();
        } catch (IOException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        }

        PopupMessage("Member user had been created");

    }//GEN-LAST:event_AddbtnActionPerformed

