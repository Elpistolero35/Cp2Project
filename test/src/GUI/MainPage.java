    private void AddEmpbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddEmpbtnActionPerformed
        // TODO add your handling code here:
        final String[] txt = {"First Name", "Surname"};
        final String[] all = {"First Name", "Surname", "Salary", "Address", "Phone Number"};

        for (int i = 0; i < addEmployeeAllTxt().size(); i++) {
            if (!isfilled(addEmployeeAllTxt().get(i), all[i])) {
                return;
            }
        }

        for (int i = 0; i < txt.length; i++) {
            if (!containsOnlyLetters(addEmployeeAllTxt().get(i), txt[i])) {
                return;
            }
        }

        if (!numbersOnly(txtSalaryEmpInput)) {
            return;
        }

        if (!numbersOnly(txtPhoneNumberEmpInput)) {
            return;
        }

        if (!EmpMalebtn.isSelected() & !EmpFemalebtn.isSelected()) {
            PopupMessage("please choose a gender");
            return;
        }

        if (!RegularEmpbtn.isSelected() & !TrainerEmpbtn.isSelected()) {
            PopupMessage("please choose a Member type");
            return;
        }

        String fname = txtFirstNameEmpInput.getText();
        String surname = txtSurnameEmpInput.getText();
        String salary = txtSalaryEmpInput.getText();
        String address = txtAddressEmpInput.getText();
        String phone = txtPhoneNumberEmpInput.getText();
        String gender = EmpMalebtn.isSelected() ? "Male" : "Female";

        if (RegularEmpbtn.isSelected()) {
            Employee emp = new Employee(fname, surname, address, phone, salary, gender);
            GymSystem.getEmployeesList().add(emp);
        } else {
            Employee emp = new PersonalTrainer(fname, surname, address, phone, salary, gender);
            GymSystem.getEmployeesList().add(emp);
        }
        try {
            GymSystem.WriteEmployee();
            Employee.saveEmpId();
        } catch (IOException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        PopupMessage("Employee added succesfuly");

    }//GEN-LAST:event_AddEmpbtnActionPerformed

